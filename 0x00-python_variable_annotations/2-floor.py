#!/usr/bin/env python3
"""Module for calculating the floor of a float.

This module provides a function that returns the floor of a given float number.
"""


def floor(n: float) -> int:
    """Calculate the floor of a float number.

    Args:
        n (float): The float number to calculate the floor of.

    Returns:
        int: The floor of `n`.
    """
    return int(n)
